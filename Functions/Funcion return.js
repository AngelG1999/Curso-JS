/*
Una función puede tener una declaración return es opcional . Se utiliza para devolver un valor de la función.
Esta declaración es útil cuando se hacen cálculos que requieren un resultado.

Cuando JavaScript alcanza una declaración de return , la función deja de ejecutarse.


ejemplos:

1-
function mifuncion(a,b){
    return a*b;
}
var x=mifuncion(5,6)
document.write(x)

2-
function addnumero(a,b){
    var c=a+b;
    return c;
}
document.write(addnumero(2,2));

*/